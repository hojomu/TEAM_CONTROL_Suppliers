package control.suppliers.model;


public class AdminGraphVO {
	private int orderAmount;
	private int totTurnover;
	private int customerNum;
	private int dpoOption;
	
	public int getOrderAmount() {
		return orderAmount;
	}
	public void setOrderAmount(int orderAmount) {
		this.orderAmount = orderAmount;
	}
	public int getTotTurnover() {
		return totTurnover;
	}
	public void setTotTurnover(int totTurnover) {
		this.totTurnover = totTurnover;
	}
	public int getCustomerNum() {
		return customerNum;
	}
	public void setCustomerNum(int customerNum) {
		this.customerNum = customerNum;
	}
	public int getDpoOption() {
		return dpoOption;
	}
	public void setDpoOption(int dpoOption) {
		this.dpoOption = dpoOption;
	}
	
	
}
